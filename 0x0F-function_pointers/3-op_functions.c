#include "3-calc.h"

/**
 * op_add - calculates sum of two ints
 * @i: first input
 * @j: second input
 * Return: Sum
 */

int op_add(int i, int j)
{
	return (i + j);
}

/**
 * op_sub - calculates diffrence of two values
 * @i: first input
 * @j: second input
 * Return: diff
 */

int op_sub(int i, int j)
{
	return (i - j);
}

/**
 * op_mul - calculates multiplication of two values
 * @i: first input
 * @j: second input
 * Return: product
 */

int op_mul(int i, int j)
{
	return (i * j);
}

/**
 * op_div - calculates divsion of two values
 * @i: first inpui
 * @j: second input
 * Return: Division
 */

int op_div(int i, int j)
{
	return (i / j);
}

/**
 * op_mod - calculates modualr division of two values
 * @i: first input
 * @j: second input
 * Return: mod
 */

int op_mod(int i, int j)
{
	return (i % j);
}
